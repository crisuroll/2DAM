lados = int(input("Introduce el número de lados: "))

match lados:
    case 1:
        print("Círculo, figura de un lado.")
    case 2:
        print("Dígono, figura de dos lados.")
    case 3:
        print("Triángulo, figura de tres lados.")
    case 4:
        print("Cuadrado, figura de cuatro lados.")
    case 5:
        print("Pentágono, figura de cinco lados.")
    case 6:
        print("Hexágono, figura de seis lados.")
    case 7:
        print("Heptágono, figura de siete lados.")
    case 8:
        print("Octógono, figura de ocho lados.")
    case 9:
        print("Eneágono, figura de nueve lados.")